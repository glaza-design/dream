first = float(input("Введіть перше число: "))
second = float(input("Введіть друге число: "))

sum_result = first + second
subtract_result = first - second
product_result = first * second

print("Результат:")
print(f"Сума: {sum_result}")
print(f"Різниця: {subtract_result}")
print(f"Добуток: {product_result}")